import 'package:flutter/widgets.dart';

/// Нижний отступ, который экран обязан оставить под системной зоной.
///
/// Нижняя панель приложения (меню + мини-плеер) **непрозрачная**, и
/// `MainScaffold` идёт с `extendBody: false` — значит `Scaffold` сам укладывает
/// тело экрана НАД панелью, и её высоту компенсировать не нужно. Внутри шелла
/// это значение равно нулю, и так и должно быть.
///
/// Смысл хелпера — экраны, открытые ПОВЕРХ шелла (карточка книги, читалка,
/// подборка): у них своего нижнего меню нет, и снизу их поджимает системная
/// зона жестов. Для них это её высота.
///
/// Использовать в нижнем padding'е прокручиваемого содержимого:
/// ```dart
/// padding: EdgeInsets.fromLTRB(20, 0, 20, 28 + context.bottomBarInset)
/// ```
///
/// Если панель когда-нибудь снова станет стеклянной и контент начнёт заезжать
/// под неё (`extendBody: true`), это же значение автоматически станет равно
/// высоте панели — и экраны не придётся переписывать.
extension SeeUInsets on BuildContext {
  double get bottomBarInset => MediaQuery.paddingOf(this).bottom;
}
